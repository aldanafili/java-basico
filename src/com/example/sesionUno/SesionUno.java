package com.example.sesionUno;

public class SesionUno {

    public static void main(String[] args) {

        //numericos
        int edad = 40;
        short antiguedad = 5;
        long dni = 35971291;

        //booleanos
        boolean verdad = true;
        boolean mentira = false;

        //texto
        char letra = 'i';
        String texto = "Primer ejercicio de java basico";

        //decimales

        float flotante = 5.5f;
        double precio = 40.59d;

        //Imprimi todos los datos vistos
        System.out.println("edad " + edad);
        System.out.println("antiguedad " + antiguedad);
        System.out.println("dni " + dni);
        System.out.println("booleanos " + verdad);
        System.out.println("booleanos " + mentira);
        System.out.println("texto " + letra);
        System.out.println("texto " + texto);
        System.out.println("decimales " + flotante);
        System.out.println("precio " + precio);
    }
}
